export default function DotHorse() {
 const ref = useRef(null);
 const frameRef = useRef(0);
 const rafRef = useRef(null);
 const lastTimeRef = useRef(0);

 useEffect(() => {
 const canvas = ref.current;
 const ctx = canvas.getContext("2d");

 const fps = 10;
 const frameDuration = 1000 / fps;

 function resize() {
 const dpr = window.devicePixelRatio || 1;

 canvas.width = canvas.clientWidth * dpr;
 canvas.height = canvas.clientHeight * dpr;

 ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
 draw();
 }

 function draw() {
 const w = canvas.clientWidth;
 const h = canvas.clientHeight;

 ctx.fillStyle = BG;
 ctx.fillRect(0, 0, w, h);

 const scale = Math.min(w, h) / 760;
 const cx = w / 2 - scale * 5;
 const cy = h * 0.46;

 ctx.fillStyle = BLUE;

 const dots = frames[frameRef.current] || frames[0];

 dots.forEach(([x, y, r]) => {
 ctx.beginPath();
 ctx.arc(cx + x * scale, cy + y * scale, r * scale, 0, Math.PI * 2);
 ctx.fill();
 });
 }

 function animate(time) {
 if (time - lastTimeRef.current >= frameDuration) {
 frameRef.current = (frameRef.current + 1) % frames.length;
 lastTimeRef.current = time;
 draw();
 }

 rafRef.current = requestAnimationFrame(animate);
 }

 resize();
 rafRef.current = requestAnimationFrame(animate);

 window.addEventListener("resize", resize);

 return () => {
 window.removeEventListener("resize", resize);
 cancelAnimationFrame(rafRef.current);
 };
 }, []);

 return (
 <canvas
 ref={ref}
 style={{
 width:"100%",
 height:"100vh",
 display:"block",
 background: BG,
 }}
 />
 );
}